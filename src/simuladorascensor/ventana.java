    private void actualizarDireccion(int valorActual, int valorDestino) {
    if (labelDireccion == null) return;
    
    if (valorDestino > valorActual) {
        labelDireccion.setText("▲");
        labelDireccion.setForeground(java.awt.Color.RED);
    } else if (valorDestino < valorActual) {
        labelDireccion.setText("▼");
        labelDireccion.setForeground(java.awt.Color.RED);
    } else {
        labelDireccion.setText("●");
        labelDireccion.setForeground(java.awt.Color.WHITE);
    }
}

    
    private void solicitarPiso(int valorDestino) {

        if (valorDestino == slider.getValue()) {
           
            return;
        }


        if (animacionEnCurso) {
         
            if (!colaDestinos.isEmpty() && estaEnMismaDireccion(valorDestino)) {
                colaDestinos.poll(); 
            }
            colaDestinos.offer(valorDestino);
           
        } else {
           
            iniciarAnimacion(valorDestino);
        }
    }


    private boolean estaEnMismaDireccion(int nuevoDestino) {
        int current = slider.getValue();
        int direccionActual = Integer.compare(destinoActual, current);
        int direccionNueva = Integer.compare(nuevoDestino, current);
        return direccionActual == direccionNueva;
    }
    
    private void actualizarPosicionFlecha(int valorSlider) {
        if (flechaAscensor == null || slider == null) return;
        
     
        int x = slider.getX() + slider.getWidth() + 10; 
        
       
        int y = calcularYDesdeValorSlider(valorSlider);
        
       
        y -= 10; 
        
     
        flechaAscensor.setLocation(x, y);
        

        flechaAscensor.setVisible(true);
      
        flechaAscensor.repaint();
    }
    
    private int calcularYDesdeValorSlider(int valor) {
        if (slider == null) return 0;
        
        int min = slider.getMinimum();
        int max = slider.getMaximum();
        int alturaSlider = slider.getHeight();
        int ySlider = slider.getY();
        

        float porcentaje = 1.0f - (float)(valor - min) / (max - min);
        

        int yEnSlider = (int)(porcentaje * alturaSlider);
        

        return ySlider + yEnSlider;
    }
   
    
    private void posicionarFlechaEnSlider() {
        if (flechaAscensor == null || slider == null) return;

     
        int valor = slider.getValue();

        int x = slider.getX() + slider.getWidth() + 15; 

     
        int y = calcularPosicionY(valor);

       
        flechaAscensor.setLocation(x, y);

      
        flechaAscensor.repaint();
    }

      
      
    private int calcularPosicionY(int valor) {
     
        int min = slider.getMinimum();  
        int max = slider.getMaximum();  
        int altura = slider.getHeight(); 
        int yBase = slider.getY();      


        float porcentaje = 1.0f - ((float)(valor - min) / (max - min));


        int posicionEnSlider = (int)(porcentaje * altura);


        return yBase + posicionEnSlider - (flechaAscensor.getHeight() / 2);
    }

        
        private void iniciarAnimacion(int valorDestino) {
            animacionEnCurso = true;
            destinoActual = valorDestino;

            int valorActual = slider.getValue();

            
            actualizarDireccion(valorActual, valorDestino);

            posicionarFlechaEnSlider();


            actualizarVisualizacionPiso(valorActual);

            if (valorActual == valorDestino) {
                iniciarPausa(valorDestino);
                return;
            }

            int duracionTotal = 2000; 
            int distancia = Math.abs(valorDestino - valorActual);
            int intervalo = Math.max(20, duracionTotal / distancia);

            if (timerAnimacion != null && timerAnimacion.isRunning()) {
                timerAnimacion.stop();
            }

            timerAnimacion = new Timer(intervalo, e -> {
                int currentValue = slider.getValue();
                int nuevoValor = currentValue;

                if (valorDestino > currentValue) {
                    nuevoValor = currentValue + 1;
                } else if (valorDestino < currentValue) {
                    nuevoValor = currentValue - 1;
                }

  
                slider.setValue(nuevoValor);

       
                posicionarFlechaEnSlider();

       
                actualizarLabelPiso(nuevoValor);


                actualizarVisualizacionPiso(nuevoValor);

               
                if (slider.getValue() == valorDestino) {
                    timerAnimacion.stop();

        
                    slider.setValue(valorDestino);


                    posicionarFlechaEnSlider();

            
                    actualizarVisualizacionPiso(valorDestino);

                    Timer timerFinal = new Timer(100, ev -> {
                        iniciarPausa(valorDestino);
                    });
                    timerFinal.setRepeats(false);
                    timerFinal.start();
                }
            });

            timerAnimacion.start();
    
        }
    
    
    private void iniciarPausa(int valorDestino) {


        slider.setValue(valorDestino);

        SwingUtilities.invokeLater(() -> {
            posicionarFlechaEnSlider();
            actualizarVisualizacionPiso(valorDestino); 
            actualizarLabelPiso(valorDestino); 
            flechaAscensor.repaint();
        });

 
        verificar(valorDestino);

        timerPausa = new Timer(2000, e -> {
            timerPausa.stop();
           

            SwingUtilities.invokeLater(() -> {
                posicionarFlechaEnSlider();
                actualizarVisualizacionPiso(valorDestino); 
            });

            procesarSiguiente();
        });

        timerPausa.setRepeats(false);
        timerPausa.start();
    }
