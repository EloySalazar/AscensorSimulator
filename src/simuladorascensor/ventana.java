    @Override
    public void paint(java.awt.Graphics g) {
        super.paint(g);
        // Actualizar posición de la flecha cuando se redibuja la ventana
        if (flechaAscensor != null && slider != null) {
            actualizarPosicionFlecha(slider.getValue());
        }
    }
    
    // Método para cuando se redimensiona la ventana
    private void formComponentResized(java.awt.event.ComponentEvent evt) {
 
        if (flechaAscensor != null && slider != null) {
            actualizarPosicionFlecha(slider.getValue());
        }
    }
    

    private void procesarSiguiente() {
        if (!colaDestinos.isEmpty()) {
            int siguiente = colaDestinos.poll();
           
            iniciarAnimacion(siguiente);
        } else {
            animacionEnCurso = false;
            destinoActual = -1;
           
        }
    }

    private void verificar(int valor) {

        piso1edificio.setSelected(false);
        piso2edificio.setSelected(false);
        piso3edificio.setSelected(false);
        piso1.setSelected(false);
        piso2.setSelected(false);
        piso3.setSelected(false);

      
        actualizarLabelPiso(valor);

        
        actualizarVisualizacionPiso(valor);

      
        SwingUtilities.invokeLater(() -> {
            posicionarFlechaEnSlider();
            repaint();
        });


    }

    private void actualizarLabelPiso(int valorPiso) {
       
        int numeroPiso = 0;
        if (valorPiso == 0) numeroPiso = 1;
        else if (valorPiso == 75) numeroPiso = 2;
        else if (valorPiso == 150) numeroPiso = 3;

    
        if (numero != null) {
            numero.setText(String.valueOf(numeroPiso));
        }
    }
    
    private void actualizarVisualizacionPiso(int valorPiso) {

        resetearColoresLabels();


        if (valorPiso == 0) {
            label_llamar1.setForeground(java.awt.Color.RED);
        } else if (valorPiso == 75) {
            label_llamar2.setForeground(java.awt.Color.RED);
        } else if (valorPiso == 150) {
            label_llamar3.setForeground(java.awt.Color.RED);
        }
    }

    
    private void resetearColoresLabels() {
        label_llamar1.setForeground(java.awt.Color.getColor("254 254 254"));
        label_llamar2.setForeground(java.awt.Color.getColor("254 254 254"));
        label_llamar3.setForeground(java.awt.Color.getColor("254 254 254"));
    }
    

    public void limpiarCola() {
        colaDestinos.clear();
        animacionEnCurso = false;
        if (timerAnimacion != null) timerAnimacion.stop();
        if (timerPausa != null) timerPausa.stop();
        
        // Deseleccionar todos los botones
        piso1edificio.setSelected(false);
        piso2edificio.setSelected(false);
        piso3edificio.setSelected(false);
        piso1.setSelected(false);
        piso2.setSelected(false);
        piso3.setSelected(false);
        
      
    }   
    // Método para obtener estado actual
    public String obtenerEstado() {
        return "Animación: " + (animacionEnCurso ? "En curso" : "Detenida") + 
               " | Cola: " + colaDestinos + 
               " | Destino actual: " + destinoActual;
    }   
    
